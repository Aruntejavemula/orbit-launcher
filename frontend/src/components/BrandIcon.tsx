export default function BrandIcon({ slug, color, size = 28, className = "", iconKey }: Props) {
  const [failed, setFailed] = useState(false);
  const Generic = iconByKey(iconKey);

  if (Generic) {
    return (
      <span
        style={{ width: size, height: size, display: "inline-grid", placeItems: "center", color: `#${color}` }}
        className={className}
      >
        <Generic size={Math.round(size * 0.78)} strokeWidth={2.1} />
      </span>
    );
  }

  const inline = INLINE[slug];
  if (inline) {
    return (
      <span style={{ width: size, height: size, display: "inline-grid", placeItems: "center" }} className={className}>
        {inline(color)}
      </span>
    );
  }

  if (!failed) {
    return (
      <img
        src={`https://cdn.simpleicons.org/${slug}/${color}`}
        alt=""
        width={size}
        height={size}
        className={className}
        onError={() => setFailed(true)}
      />
    );
  }

  return (
    <span
      className={`grid place-items-center rounded-md font-bold text-white ${className}`}
      style={{ width: size, height: size, background: `#${color}`, fontSize: size * 0.5 }}
    >
      {slug.slice(0, 1).toUpperCase()}
    </span>
  );
}
